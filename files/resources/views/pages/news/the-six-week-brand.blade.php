    <p class="lead">Six weeks is not a discount. It is a constraint we picked on purpose, because the alternative — the six-month brand — fails in a way that is very hard to see while it is happening.</p>

    <h2>What long engagements actually buy</h2>

    <p>More rounds. Not more clarity, more rounds. The extra months get spent revisiting decisions that were already good in week three, because there is time to revisit them and nobody wants to be the person who says stop.</p>

    <p>By month four the work is not better, it is just more agreed with. Those are different things, and only one of them shows up in the launch.</p>

    <blockquote>
        <p>A shipped eight is worth more than a stalled ten.</p>
    </blockquote>

    <h2>What the deadline forces</h2>

    <p>A hard date turns opinions into decisions. It also changes who shows up: when everyone knows there are six weeks, the founder comes to the calls, and the calls are about the thing rather than about scheduling the next call.</p>

    <p>The shape is always roughly the same:</p>

    <ol>
        <li><strong>Week one — discovery.</strong> Interviews, teardowns, and the uncomfortable questions. You leave with a positioning line you can repeat.</li>
        <li><strong>Weeks two and three — direction.</strong> One real direction, in the browser, not a mood board. We iterate on it in public.</li>
        <li><strong>Weeks four and five — the system.</strong> Type, colour, components, and the rules that keep it honest.</li>
        <li><strong>Week six — ship.</strong> Live, measured, and yours.</li>
    </ol>

    <h2>When it does not work</h2>

    <p>We are not precious about this. Six weeks fails when the decision-maker cannot make the calls in week one, when the product is still changing underneath us, or when there are four stakeholders who each need to win something. In those cases the constraint just relocates the pain, and we would rather say so on the intro call than in week five.</p>

    <h2>The part nobody expects</h2>

    <p>Clients almost never ask for more time at the end. They ask what is next — which is the whole point. The launch is the midpoint, not the finish line, and a studio that is still there in month three is worth more than one that was there for month three.</p>

